public class IncrementAndDecrementOperators {
    public static void main(String[] args) {
       int sumit = 75;
       int naresh = 60;
       int kapil = 33;
       sumit += 7;
       naresh -= 5;
       kapil += 10;
       System.out.println(sumit);
       System.out.println("sumit");

       System.out.println(naresh);
       System.out.println("naresh");

       System.out.println("kapil");
       System.out.println(kapil++);
       System.out.println("kapil post increment");
       System.out.println(kapil);
    }
}
